for model_name, scoring in SCORES.items():
    fpath = f"{RESULT_DIR}/{model_name}_results.json"
    with open(fpath, 'r', encoding='utf-8') as f:
        data = json.load(f)

    results = data['results']
    scores_list = scoring['scores']
    comments_list = scoring['comments']

    # Bug fix 1: validate labels match expected order before writing any scores
    if len(results) != len(EXPECTED_LABELS):
        raise ValueError(f"{model_name}: expected {len(EXPECTED_LABELS)} cases, got {len(results)}")
    for i, (result, expected_label) in enumerate(zip(results, EXPECTED_LABELS)):
        actual_label = result.get('label', '')
        if actual_label != expected_label:
            raise ValueError(
                f"{model_name} case {i}: label mismatch!\n"
                f"  expected: {expected_label[:60]}\n"
                f"  actual:   {actual_label[:60]}"
            )

    for i, (result, case_scores, comment) in enumerate(zip(results, scores_list, comments_list)):
        judgment = result['judgment']

        new_scores = {}
        new_reasons = {}
        for j, dim in enumerate(DIMS):
            new_scores[dim] = case_scores[j]
            new_reasons[dim] = "Claude Code人工评分"

        judgment['scores'] = new_scores
        judgment['reasons'] = new_reasons
        judgment['overall_comment'] = comment
        judgment['judge'] = "claude-code-manual"
        # Bug fix 2: preserve original run status, mark judged separately
        result['original_run_status'] = result.get('status', '')
        result['judged'] = True

    # Compute averages
    total_avg = {}
    for dim in DIMS:
        vals = [r['judgment']['scores'][dim] for r in results]
        total_avg[dim] = round(sum(vals) / len(vals), 2)

    overall = round(sum(total_avg.values()) / len(DIMS), 2)

    data['summary'] = {
        'avg_scores': total_avg,
        'overall_score': overall,
        'judge': 'claude-code-manual',
        'total_cases': len(results)
    }

    with open(fpath, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

